        ConsultaControl controlador = new ConsultaControl();
        if(controlador.verificarConsultaConFacturaPendiente(idConsulta)){
            frmPadre.actualizarPanelContenido(new RegistrarVenta(frmPadre, idConsulta));
        }else{
            setMensaje("error", "El ID de la consulta proporcionada ya tiene factura, escoja uno de la tabla");
        }

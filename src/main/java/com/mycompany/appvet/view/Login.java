    private void setMensaje(String tipo, String contenido){
        if(tipo.equals("error")){
            JOptionPane.showMessageDialog(null, contenido, "LOGIN - ERROR",
		JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private boolean verificacion(){
        if(txtUser.getText().equals("")){
            setMensaje("error","Debe ingresar el usuario");
            return false;
        }
        if(txtPass.getText().equals("")){
            setMensaje("error","Debe ingresar la contraseña");
            return false;
        }
        return true;
    }
    
    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        if(verificacion()){
            user = txtUser.getText();
            pass = txtPass.getText();
            
            try{
                EmpleadoControl controlador = new EmpleadoControl();
                Empleado e = controlador.loginEmpleado(user, pass);
                if(e.getId_empleado().equals("-1")){
                    setMensaje("error", "Usuario o contraseña incorrectos");
                }else{
                    MenuPrincipal menu = new MenuPrincipal(e);
                    menu.setVisible(true);
                    this.dispose();
                }
            }catch(Exception ex){
                setMensaje("error", ex.getMessage());
            }
        }
        
    }//GEN-LAST:event_btnIngresarActionPerformed

